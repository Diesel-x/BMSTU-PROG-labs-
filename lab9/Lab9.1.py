from math import sqrt
from Matrix import *

# a = [2, 4, 6]
# b = [1, 3, 5, 7]

a = list(map(int, input("–í–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–∞ —á–µ—Ä–µ–∑ –ø—Ä–æ–±–µ–ª –¥–ª—è –º–∞—Å—Å–∏–≤–∞ –∞: ").split()))
b = list(map(int, input("–í–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–∞ —á–µ—Ä–µ–∑ –ø—Ä–æ–±–µ–ª –¥–ª—è –º–∞—Å—Å–∏–≤–∞ b: ").split()))
matrix = [ [] for i in range(len(a))]
s = []

for i in range(len(a)):
    c = 0
    for j in range(len(b)):
        matrix[i].append(a[i] * b[j])
        if matrix[i][j] >= 0 and sqrt(matrix[i][j]) == int(sqrt(matrix[i][j])):
            c += 1
    s.append(c)

print_matrix(matrix)
print("–°–ø–∏—Å–æ–∫ –ø–æ–ª–Ω—ã—Ö –∫–≤–∞–¥—Ä–∞—Ç–æ–≤ –≤ –º–∞—Ç—Ä–∏—Ü–µ: ", end="")
print(*s, sep=", ")